class BedtimeGuide:
    """
    睡前引导系统
    
    为每个孩子生成30分钟以上的睡前仪式内容。
    包含：慢节奏引导词 + 舒缓背景音乐 + 自动时长扩展
    """
    
    def __init__(self):
        self.scripts = BEDTIME_SCRIPTS
        self.expansions = EXPANSION_JOURNEYS
    
    def generate_bedtime_session(self, child_profile, duration_minutes=30, custom_notes=None) -> Dict:
        """
        生成完整的睡前仪式会话
        
        保证实际朗读时长达到目标时长（默认30分钟）。
        如果基础内容不足，自动插入想象旅程扩展段落。
        """
        stage = child_profile.stage
        script_template = self.scripts.get(stage, self.scripts["S4"])
        
        # 构建仪式
        session = {
            "title": script_template["title"],
            "music": script_template["music"],
            "child_name": child_profile.name,
            "date": datetime.now().strftime("%Y-%m-%d"),
            "total_duration": 0,
            "sections": [],
            "has_expansion": False,
        }
        
        full_text_lines = []
        
        # 第一步：生成基础段落
        for section in script_template["sections"]:
            lines = section["lines"].copy()
            
            # 如果有特殊情况，插入个性化语句
            if custom_notes and section["name"] in ["情绪安抚", "情绪清理", "压力释放", "情绪释放"]:
                lines.insert(1, f"今天...{custom_notes}...")
            
            # 加入停顿标记
            lines_with_pause = []
            for line in lines:
                if not line.endswith("...") and not line.endswith("？") and not line.endswith("?"):
                    line = line + "..."
                lines_with_pause.append(line)
            
            session["sections"].append({
                "name": section["name"],
                "lines": lines_with_pause,
                "duration": section["duration"],
            })
            
            full_text_lines.extend(lines_with_pause)
            full_text_lines.append("")
        
        # 第二步：检查时长，如果不足则自动扩展
        base_text = "\n".join(full_text_lines)
        base_duration = self.estimate_actual_duration(base_text)
        
        if base_duration < duration_minutes:
            # 需要扩展
            needed_minutes = duration_minutes - base_duration
            expansion_sections = self._build_expansion_sections(needed_minutes)
            
            # 将扩展段落插入到"睡眠引导"之前
            insert_idx = len(session["sections"])
            for i, sec in enumerate(session["sections"]):
                if sec["name"] in ["睡眠引导", "入眠"]:
                    insert_idx = i
                    break
            
            for exp_sec in expansion_sections:
                session["sections"].insert(insert_idx, exp_sec)
                insert_idx += 1
                full_text_lines.extend(exp_sec["lines"])
                full_text_lines.append("")
            
            session["has_expansion"] = True
            print(f"  [睡前引导] 基础内容{base_duration}分钟，自动扩展{len(expansion_sections)}个想象旅程段落")
        
        session["script_text"] = "\n".join(full_text_lines)
        session["total_duration"] = self.estimate_actual_duration(session["script_text"])
        
        return session
    
    def _build_expansion_sections(self, needed_minutes):
        """构建扩展段落，补足所需时长
        
        每个想象旅程段落实际约5-6分钟。
        目标：总时长控制在30-35分钟。
        """
        sections = []
        journey_names = list(self.expansions.keys())
        
        # 每个扩展段落实际约5.5分钟
        journeys_needed = max(1, int(needed_minutes / 5.5))
        # 最多插入3个，避免过长
        journeys_needed = min(journeys_needed, 3)
        
        for i in range(journeys_needed):
            journey_name = journey_names[i % len(journey_names)]
            journey_lines = self.expansions[journey_name].copy()
            
            # 加入停顿标记
            lines_with_pause = []
            for line in journey_lines:
                if not line.endswith("...") and not line.endswith("？") and not line.endswith("?"):
                    line = line + "..."
                lines_with_pause.append(line)
            
            sections.append({
                "name": f"想象旅程·{journey_name}",
                "lines": lines_with_pause,
                "duration": 5,
            })
        
        return sections
    
    def get_script_preview(self, child_profile, max_lines=20) -> str:
        """获取引导词预览"""
        session = self.generate_bedtime_session(child_profile)
        lines = session["script_text"].split("\n")[:max_lines]
        return "\n".join(lines) + "\n... (还有内容)"
    
