    def estimate_actual_duration(self, script_text: str) -> int:
        """
        估算实际朗读时长
        
        慢节奏朗读：每分钟约80-100字
        加上停顿（"..."每个约2秒）
        """
        char_count = len(script_text.replace(" ", "").replace("\n", ""))
        pause_count = script_text.count("...")
        
        # 字数时间 + 停顿时间
        reading_time = char_count / 90 * 60  # 秒
        pause_time = pause_count * 2  # 秒
        
        total_seconds = reading_time + pause_time
        return int(total_seconds / 60)  # 转换为分钟
