def main():
    """主函数"""
    print("=== 批量生成说文解字字卡 ===")
    print(f"输出目录：{OUTPUT_DIR}")
    
    # 确保目录存在
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    
    # 生成字卡
    number = 82  # 从82开始编号
    for char, data in CHARACTERS.items():
        filepath = os.path.join(OUTPUT_DIR, f"{char}.md")
        
        # 检查文件是否已存在
        if os.path.exists(filepath):
            print(f"  跳过（已存在）：{char}.md")
            continue
        
        content = generate_card(char, data, number)
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"  已生成：{char}.md（编号：{number}）")
        number += 1
    
    print(f"\n=== 完成 ===")
    print(f"共生成 {len(CHARACTERS)} 个字卡")

if __name__ == "__main__":
    main()
